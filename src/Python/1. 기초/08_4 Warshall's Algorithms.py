def warshall(A):
    R[0] = A
    n = len(R)-1
    for k in range(1, n+1):
        for i in range(1, n+1):
            for j in range(1, n+1):
                if R[k-1][i][j] == 1 or (R[k-1][i][k] == 1 and R[k-1][k][j] == 1):
                    R[k][i][j] = 1
    return R[n]


